public class Pizza {
    
    String Bread;
    String Sauce;
    String Cheese;
    String Topping;

    Pizza(String Bread, String Sauce, String Cheese, String Topping){
        this.Bread = Bread;
        this.Sauce = Sauce;
        this.Cheese = Cheese;
        this.Topping = Topping;
    }
    Pizza(String Bread, String Sauce, String Cheese){
        this.Bread = Bread;
        this.Sauce = Sauce;
        this.Cheese = Cheese;
    }
}
